/**
 * Composable that displays an app bar and a list of heroes
 * */
@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun SuperheroesApp() {
    Scaffold(
        modifier = Modifier.fillMaxSize(),
        topBar = {
            TopAppBar()
        }
    ) {
        val heroes = HeroesDataSourceRepository.heroes
        HeroesList(heroes = heroes, contentPadding = it)
    }
}

/**
 * Composable that display a top bar with an icon and text
 *
 * @param modifier modifiers to set this composable
 * */
@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun TopAppBar(modifier: Modifier = Modifier) {
    CenterAlignedTopAppBar(
        title = {
            Text(
                text = stringResource(id = R.string.app_name),
                style = MaterialTheme.typography.displayLarge
            )
        },
        modifier = modifier
    )
}


